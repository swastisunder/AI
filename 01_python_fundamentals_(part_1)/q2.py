'''
Take two numbers as input from the user and print their sum, difference, product, and quotient
'''

num_1 = float(input("Enter number 1 : "))
num_2 = float(input("Enter number 2 : "))

print(f"Sum is : {num_1+num_2}")
print(f"Difference is : {num_1-num_2}")
print(f"Product is : {num_1*num_2}")
print(f"Quotient is : {num_1//num_2}")